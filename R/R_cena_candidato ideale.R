  candidato <- candidati[[i]]
  
  if (
    candidato[["sesso"]] %in% candidato_ideale[["sesso"]] &&
    candidato[["anni"]] %in% candidato_ideale[["anni"]] &&
    candidato[["nazionalità"]] %in% candidato_ideale[["nazionalità"]] &&
    candidato[["vacanze"]] %in% candidato_ideale[["vacanze"]] &&
    candidato[["vuole_viaggiare"]] == candidato_ideale[["vuole_viaggiare"]]
